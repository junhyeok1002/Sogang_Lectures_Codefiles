w = int(input('거래금액을 입력하세요(천원 단위) :'))
w = 1000 * w
if( w >= 50000000 ):
    c = 20000 + w*0.09/100
elif ( 5000000 <= w):
    c = 15000 + w*0.11/100
elif ( 2000000 <= w):
    c = 10000 + w*0.22/100
elif ( 600000 <= w):
    c = 8000 + w*0.34/100
elif ( 250000 <= w):
    c = 6000 + w*0.66/100
elif ( 250000 > w):
    c = 3000 + w*1.7/100
    if (c <= 5000):
        c = 5000
w = int(w / 1000)
c = int(c)
print('거래액 {}천원에 대해 지불할 중개료는 {}원입니다.'.format(w, c))
